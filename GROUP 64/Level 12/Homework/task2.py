# შედარების ოპერაციები და მათი შედეგები
a = 10 > 5   # True 
b = 3 < 2    # False 
c = 7 == 7   # True 
d = 9 != 4   # True 
e = 8 >= 8   # True 

print(a, b, c, d, e)


#ოპერატორი არის სიმბოლო ან სიმბოლოების კომბინაცია, რომელიც განსაზღვრავს მოქმედებას რიცხვებს შორის. მაგალითად, > (მეტობა), < (ნაკლებობა), == (თანაბრობა) — ესენი არიან შედარების ოპერატორები.

#ოპერაცია არის პროცესი, რომელიც სრულდება ოპერატორის გამოყენებით. მაგალითად, 10 > 5 არის შედარების ოპერაცია, რომელიც აბრუნებს True-ს